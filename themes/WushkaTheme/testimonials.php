<script>
  var work = document.querySelector(".stories-block");
  var items = Array.from(work.querySelectorAll(".single-story"));
  var loadMore = document.getElementById("loadMore");
  var loadLess = document.getElementById("loadLess");
  maxItems = 7;
  loadItems = 1;
  hiddenClass = "hiddenStyle";
  hiddenItems = Array.from(document.querySelectorAll(".hiddenStyle"));
  
  loadLess.style.display = "none";

  items.forEach(function (item, index) {
    console.log(item.innerText, index);
    if (index > maxItems - 1) {
      item.classList.add(hiddenClass);
    }
  });

  loadMore.addEventListener("click", function () {
    [].forEach.call(document.querySelectorAll("." + hiddenClass), function (
      item,
      index
    ) {
      if (index < loadItems) {
        item.classList.remove(hiddenClass);
      } 

      if (document.querySelectorAll("." + hiddenClass).length === 0) {
        loadMore.style.display = "none";
        loadLess.style.display = "block";
      }
    });
  });

  loadLess.addEventListener("click", function () {
    items.forEach(function (item, index) {
      console.log(item.innerText, index);
      if (index > 6) {
        item.classList.add(hiddenClass);
        loadMore.style.display = "block";
        loadLess.style.display = "none";
      }
    });
  }); 

</script>
